package Day2_04112020;

public class for_loop {
    public static void main(String[] args) {

        String [] states = new String[4];
        states [0] = "New York";
        states [1] = "California";
        states [2] = "Ohio";
        states [3] = "Arizona";

        //print all the states from 0 to 3 in sequence
        //i++ each time adding +1 to i
        //i represents your initial/Starting points of your index
        for (int i = 0; i < states.length; i++) {
            System.out.println("My states is " + states[i]);



        }//end of for loop



    }//end of the main method



    }//end of java class
